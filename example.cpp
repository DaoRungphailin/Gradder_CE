#include<stdio.h>

main()
{
	int x = 9; // 9 : 0000 1001
	int y = 7; // 7 : 0000 0111
	printf("x(%d) & y(%d) = %d\n", x, y, x & y);
	x = 12; // 12 : 0000 1100
	y = 5; // 5 : 0000 0101
	printf("x(%d) | y(%d) = %d\n", x, y, x | y);
	//* 9 | 7 : 0000 1101 */
	x = 10; // 10 : 0000 1010
	y = 3; // 3 : 0000 0011	
	printf("x(%d) ^ y(%d) = %d\n", x, y, x ^ y);
}
//* 9 ^ 7 : 0000 1001
